public class dog {
  private   String name;
  private int count;

  public dog(String nm,int cnt)
  {
      name=nm;
      count=cnt;
  }


  public String getName()
  {
      return name;
  }

  public void setName(String nam)
  {
      name=nam;
  }

  public int getCount()
  {
      return count;
  }

  public void setCount(int p)
  {
      count=p;
  }


}
